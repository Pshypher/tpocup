# Calculates the volume of water in liters when 1 centimeter
# of water falls on 1 hectare
volume_cubic_cm = 1 * (1 * 100000000)   # Convert a hectare to square centimeter
volume_liters = volume_cubic_cm * 0.001
print("Volume of 1 cm of water on 1 hectare of space is {:.0f} liters".format(volume_liters))
