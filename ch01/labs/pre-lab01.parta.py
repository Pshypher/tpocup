AAA = 8
BBB = 20
CCC = 1
DDD = 11
EEE = 13
FFF = 5

# Give the value displayed by each of the calls to "print" below.

A = BBB // AAA
                                                    
print( "\nValue of A: ", A )     # Value of A: 2

B = BBB % AAA

print( "\nValue of B: ", B )     # Value of B: 4

C = DDD * 3 + FFF

print( "\nValue of C: ", C )     # Value of C: 38

D = EEE - FFF * 4

print( "\nValue of D: ", D )     # Value of D: -7

E = EEE // FFF + CCC

print( "\nValue of E: ", E )     # Value of E: 3

F = EEE // (FFF + CCC)

print( "\nValue of F: ", F )     # Value of F: 2

G = FFF // 2 * 2

print( "\nValue of G: ", G )     # Value of G: 4
