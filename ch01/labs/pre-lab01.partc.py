AAA = 9
BBB = 13

XXX = 17.9
YYY = 1.2e+3
ZZZ = 5.625e-12

# Give the value displayed by each of the calls to "print" below.

A = int( XXX )

print( "\nValue of A: ", A )     # Value of A: 17

B = int( XXX + 4.8 )

print( "\nValue of B: ", B )     # Value of B: 22

C = int( XXX - AAA // 2 )

print( "\nValue of C: ", C )     # Value of C: 13

D = int( ZZZ )

print( "\nValue of D: ", D )     # Value of D: 0

E = int( YYY - ZZZ )

print( "\nValue of E: ", E )     # Value of E: 1199

X = float( BBB )

print( "\nValue of X: ", X )     # Value of X: 13.0

Y = float( BBB // 2 )

print( "\nValue of Y: ", Y )     # Value of Y: 6.0
